
import Foundation

struct VideoMetadata {
    var title: String
    var channel: String
    var date: String
    var url: String
}

class AIContentService {
    static let shared = AIContentService()
    
    enum AIError: Error {
        case missingKey
        case invalidResponse
        case apiError(String)
    }
    
    func generateArticle(transcript: String, metadata: VideoMetadata, apiKey: String, provider: String = "OpenAI", model: String = "gpt-4o", completion: @escaping (Result<Article, Error>) -> Void) {
        guard !apiKey.isEmpty else {
            completion(.failure(AIError.missingKey))
            return
        }
        
        let prompt = """
        You are an expert tech journalist. Write a detailed, authoritative blog post based on the following YouTube video transcript.
        
        Video Title: \(metadata.title)
        Channel: \(metadata.channel)
        Date: \(metadata.date)
        Original URL: \(metadata.url)
        
        Instructions:
        1. Write a catchy, SEO-optimized Title.
        2. Write a detailed, structured blog post (HTML format: use <h3>, <p>, <ul>, <table>).
        3. Do NOT output a full HTML document (no <html> or <body> tags), just the content.
        4. Reference the YouTuber ("According to [Channel]...") as the authoritative source.
        5. Verify facts from the transcript.
        6. Summary: Provide a 2 sentence SEO meta description at the very end, separated by "||SUMMARY||".
        
        Transcript:
        \(transcript.prefix(20000)) 
        """
        
        // Determine Endpoint
        let isOpenRouter = provider == "OpenRouter"
        let endpoint = isOpenRouter ? "https://openrouter.ai/api/v1/chat/completions" : "https://api.openai.com/v1/chat/completions"
        let url = URL(string: endpoint)!
        
        var request = URLRequest(url: url)
        request.httpMethod = "POST"
        request.addValue("Bearer \(apiKey)", forHTTPHeaderField: "Authorization")
        request.addValue("application/json", forHTTPHeaderField: "Content-Type")
        
        if isOpenRouter {
            request.addValue("https://webmakr.app", forHTTPHeaderField: "HTTP-Referer")
            request.addValue("WebMakr", forHTTPHeaderField: "X-Title")
        }
        
        // Adjust model name if needed (OpenRouter usually expects 'vendor/model', e.g. 'anthropic/claude-3-opus')
        // But user might just pass 'claude-3-opus'. OpenRouter mapping usually handles standard names or full names.
        // We will trust the input 'model' string.
        
        let body: [String: Any] = [
            "model": model,
            "messages": [
                ["role": "system", "content": "You are a helpful expert coding assistant."],
                ["role": "user", "content": prompt]
            ],
            "temperature": 0.7
        ]
        
        request.httpBody = try? JSONSerialization.data(withJSONObject: body)
        
        URLSession.shared.dataTask(with: request) { data, response, error in
            if let error = error {
                completion(.failure(error))
                return
            }
            
            // Debug print
            if let data = data, let str = String(data: data, encoding: .utf8) {
                print("AI Response: \(str)")
            }
            
            guard let data = data else {
                completion(.failure(AIError.invalidResponse))
                return
            }
            
            // Parse OpenAI Response
            do {
                if let json = try JSONSerialization.jsonObject(with: data) as? [String: Any] {
                    if let errorMsg = json["error"] as? [String: Any] {
                        let message = errorMsg["message"] as? String ?? "Unknown API Error"
                        completion(.failure(AIError.apiError(message)))
                        return
                    }
                    
                    if let choices = json["choices"] as? [[String: Any]],
                       let first = choices.first,
                       let message = first["message"] as? [String: Any],
                       let content = message["content"] as? String {
                        
                        // Parse Content (Title? Body? Summary?)
                        var cleanContent = content.replacingOccurrences(of: "```html", with: "").replacingOccurrences(of: "```", with: "")
                        
                        // Split Summary
                        var summary = "Generated by AI"
                        if let range = cleanContent.range(of: "||SUMMARY||") {
                            summary = String(cleanContent[range.upperBound...]).trimmingCharacters(in: .whitespacesAndNewlines)
                            cleanContent = String(cleanContent[..<range.lowerBound])
                        }
                        
                        // Extract Title
                        var title = metadata.title
                        let lines = cleanContent.components(separatedBy: "\n").filter { !$0.isEmpty }
                        if let firstLine = lines.first, firstLine.lowercased().hasPrefix("title:") {
                            title = firstLine.replacingOccurrences(of: "Title:", with: "").trimmingCharacters(in: .whitespaces)
                            if let range = cleanContent.range(of: firstLine) {
                                cleanContent.removeSubrange(range)
                            }
                        }
                        
                        let article = Article(
                            title: title,
                            slug: metadata.title.lowercased().replacingOccurrences(of: " ", with: "-").filter { "abcdefghijklmnopqrstuvwxyz0123456789-".contains($0) },
                            summary: summary,
                            contentHTML: cleanContent,
                            author: "AI (Source: \(metadata.channel))"
                        )
                        
                        completion(.success(article))
                        
                    } else {
                        completion(.failure(AIError.apiError("Invalid JSON structure")))
                    }
                }
            } catch {
                completion(.failure(error))
            }
        }.resume()
    }
}
